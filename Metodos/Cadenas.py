#Estos son los metodos con cadenas en python

# DIR - decuelve la lista de atributos y metodos de un objeto
# UPPER - convierte una cadena a mayusculas
#  LOWER - convierte una cadena a minusculas
# CAPITALIZE - convierte la primera letra de una cadena a mayuscula
# 
# FIND - busca una subcadena en una cadena y devuelve la posicion de la primera ocurrencia si no devuelve -1
# INDEX - busca una subcadena en una cadena y devuelve la posicion de la primera ocurrencia si no devuelve error
# 
# ISNUMERIC - devuelve True si la cadena es numerica
# ISALPHA - devuelve True si la cadena es alfabetica
# 
# COUNT - cuenta cuantas veces aparece una subcadena en una cadena
# LEN - devuelve la longitud de una cadena
# 
# ENDSWITH - devuelve True si una cadena termina con una subcadena
# STARTSWITH - devuelve True si una cadena empieza con una subcadena
#
# REPLACE - reemplaza una subcadena por otra en una cadena
# SPLIT - divide una cadena en una lista de subcadenas
